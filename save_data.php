<?php
// Перевіряємо, чи запит методом POST
if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    // Отримуємо дані з форми
    $name = $_POST['name'];
    $email = $_POST['email'];

    // Відкриваємо файл для запису в режимі "append", але з розширенням .csv
    $file = fopen('subscribers.csv', 'a'); // Переконайтесь, що файл має розширення .csv
    if ($file) {
        // Записуємо дані у CSV файл
        fputcsv($file, array($name, $email));
        fclose($file); // Закриваємо файл

        // Виводимо повідомлення про успіх
        echo "<p>Дякуємо за підписку!</p>";
    } else {
        // Якщо файл не вдалося відкрити
        echo "<p>Помилка при збереженні даних!</p>";
    }
}
?>
